package main 

import "fmt"

	func main(){

		var a string = "inicial" //Declarando variable "inicial"
		fmt.Println(a)

		//Declarando mas de una variable
		var b,c int = 4, 5
		fmt.Println(c+b)

		//Declarando variables booleanas
		var d = true
		fmt.Println(d)

		var e int
		fmt.Println(e)

		f:= "short"
		fmt.Println(f)
	}
